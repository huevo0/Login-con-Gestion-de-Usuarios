    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String usu = txtUsuario.getText();
        String contrasenia = txtContrasenia.getText();
        String rol = (String) cmbRol.getSelectedItem();
        
        control.editarUsuario(usuario,usu,contrasenia, rol);
    
        mostrarMensaje("Usuario Editado Corectamente", "info", "Edicion de Usuario");
        
        this.dispose();
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        usuario = control.traerUsuario(idUsuario);
        txtUsuario.setText(usuario.getNombreUsuario());
        txtContrasenia.setText(usuario.getContrasenia());
        //Cargar roles
        List<Rol> listaRoles = control.traerRoles();
        
        if(listaRoles!=null){
        for (Rol rol : listaRoles) {
            cmbRol.addItem(rol.getNombreRol());
            
        }
        }
        //marcar seleccionar roles
        String rol = usuario.getUnRol().getNombreRol();
        int cantidadItems = cmbRol.getItemCount();
        for (int i = 0; i < cantidadItems; i++) {
            if(String.valueOf(cmbRol.getItemAt(i)).equals(rol)){
                cmbRol.setSelectedIndex(i);
            }
        }
    }//GEN-LAST:event_formWindowOpened

